package org.rapid7.nexpose.api;

/**
 * Interface for the API error handlers.
 *
 * @author Leonardo Varela
 */
public interface IAPIErrorHandler
{
   /**
    * Handles an error on an API response
    *
    * @param request the request that caused the response
    * @param response the response that contains error(s).
    * @param session the session in which the error was caused.
    * @param message the message to be thrown in the APIException.
    * @throws APIException throws an exception when the exceptions was not
    *         anticipated.
    */
   void handleError(APIRequest request, APIResponse response,
      Session session, String message) throws APIException;

}
